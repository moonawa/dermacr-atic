    <h3>Comment faire pour arrêter de se gratter ?</h3>
    <p>Pour pouvoir calmer votre peau qui gratte, vous devez trouver l’origine de vos démangeaisons. En attendant une consultation médicale, vous pouvez adopter les bons gestes pour limiter le prurit, autre terme utilisé pour parler de démangeaisons cutanées.</p>
    <h3>Nos conseils pour calmer les démangeaisons :</h3>
    <p>Tailler vos ongles afin d’éviter de vous blesser en vous grattant.
        Utiliser une compresse froide et rincer vos yeux sous l’eau avant de prendre un traitement ophtalmique.
        Utiliser une crème hydratante et des produits de soin adaptés à votre peau.
        Utiliser des savons doux et naturels et éviter les lessives trop chimiques pour votre linge.
        Vous laver les mains surtout si vous vous grattez l’oeil.</p>
