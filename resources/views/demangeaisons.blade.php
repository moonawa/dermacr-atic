    <h1>Comment calmer les démangeaisons ?</h1>
    <p>Vous avez la peau qui démange et vous n’arrêtez pas de vous gratter ? Ces démangeaisons peuvent être localisées sur différents endroits du corps. En plus, ce prurit peut provoquer l’apparition de plaques rouges de cloques ou de boutons. Quelle que soit l’origine de vos démangeaisons, un diagnostic médical s’impose lors d’une visite chez un dermatologue. L’équipe médicale de Qare vous explique les bons gestes à adopter pour calmer vos démangeaisons.</p>
