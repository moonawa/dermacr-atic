            <h2>L’acné et ses différentes formes</h2>
            <p>L’acné est une dermatose bénigne mais inesthétique causée par une production excessive de sébum qui bouche les pores et entraîne l’apparition de boutons inflammatoires et disgracieux, le plus souvent sur le visage, le dos et le torse. Ce problème dermatologique survient majoritairement à la puberté mais disparaît de lui-même à la fin de l’adolescence. Il touche environ 70 % des adolescents, en majorité les garçons.

            Bon à savoir : l’acné est un problème de peau fréquent qui ne nécessite pas forcément de consultation en présentiel. N’hésitez pas à consulter un médecin en vidéo pour trouver un traitement qui vous convient.</p>
